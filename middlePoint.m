function mp = middlePoint(A, B)
% FUNCTION NAME:
%   middlePoint
%
% DESCRIPTION:
%   Calculates the middle point between two given points
%   
% INPUT:
%   A - (vector) point with 2 coordinates
%   B - (vector) point with 2 coordinates
%
% OUTPUT:
%   mp - (vector) middle point
%
% AUTHOR
%   Vinicius Almada
%
mp = (A + B) / 2;
end